-- ═══════════════════════════════════════════════════════════════════
-- Настройки приложения (переопределение .env через веб-интерфейс).
-- Пользователь настраивает модели, URL, таймауты и т.д. в админке.
-- ═══════════════════════════════════════════════════════════════════

CREATE TABLE IF NOT EXISTS app_settings (
    key   TEXT PRIMARY KEY,
    value_json TEXT NOT NULL DEFAULT 'null'
);

COMMENT ON TABLE app_settings IS 'Ключ-значение настроек (JSON). Приоритет над .env. Задаются через Web UI.';
